                    <h2>Maximizing Your Hawaiian Adventure</h2>
                    <p>Some things to note about Hawaii in general and the North Shore including Haleiwa Town are:
                    <ul>
                        <li><strong><u>Mostly Mild Weather</u>: </strong>The weather is mild and you're probably not going to need a jacket or pants although you may want to pack a light jacket if you're prone to getting chilly and going on a boat, especially early in the morning since the wind chill can be a little cold to some.</li>
                        <li><strong><u>Plan For Rain</u>: </strong>You never know when it's going to rain, even if it seems completely sunny, so you might want to bring an umbrella just in case.</li>
                        <li><strong><u>Higher Prices</u>: </strong>Prices can be high on the north shore and in Hawaii in general compared to other areas of the country and world, so be prepared. Packing lunches and planning spending can help you save.</li>
                        <li><strong><u>Traffic</u>: </strong>There are only a couple of ways in and out of the north shore and it's a tourist heavy destination, so plan accordingly. Busy times going in and out generally correspond with normal traffic patterns although weekends and holidays can see heavy traffic both ways all day. </li>
                        <li><strong><u>Be Respectful</u>: </strong>The residents of Oahu are a mix of locals, transplants, military, and Native Hawaiians. The culture is friendly and welcoming as long as respect is shown, so please be respectful, take the time to learn a little about the history of the islands and what aloha is, show aloha and you just may experience the magic this special place and its people have to offer.</li>
